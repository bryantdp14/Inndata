public class TiposVariables {
    
    public static void main(String[] args) {
        /*
        Autor : Bryant A. Dorado
        Fecha : 08-06-2026
        
        */

        // Declaración de variables
        byte b = 127; // Declaración de una variable de tipo byte
        
        // Tipos de variables primitivas en Java
        short s = 32767; // Declaración de una variable de tipo short (entero corto)
        int i = 2147483647; // Declaración de una variable de tipo int (entero)
        long l = 9223372036854775807L; // Declaración de una variable de tipo long (entero largo)
        
        float full = 3.14f; // Declaración de una variable de tipo float (número decimal de precisión simple)
        double d = 3.14159265359; // Declaración de una variable de tipo double (número decimal de precisión doble)
        
        boolean bool = true; // Declaración de una variable de tipo boolean (verdadero o falso)
        

        char c = 255; // Declaración de una variable de tipo char (carácter único)

        // Imprimir los valores de las variables
        System.out.println("Valor de la variable byte: " + b);
        System.out.println("Valor de la variable short: " + s);
        System.out.println("Valor de la variable int: " + i);
        System.out.println("Valor de la variable long: " + l);
        System.out.println("Valor de la variable float: " + full);
        System.out.println("Valor de la variable double: " + d);
        System.out.println("Valor de la variable boolean: " + bool);
        System.out.println("Valor de la variable char: " + c);
    }
}
